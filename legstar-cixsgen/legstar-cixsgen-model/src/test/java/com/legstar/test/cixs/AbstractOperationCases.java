package com.legstar.test.cixs;

import junit.framework.TestCase;

import com.legstar.cixs.gen.model.CixsStructure;

/**
 * Generic code common to all test cases.
 * 
 */
public abstract class AbstractOperationCases extends TestCase {

    /** Cobol binding classes package. As generated by LegStar Coxbgen. */
    public static final String JAXB_PKG_PREFIX = "com.legstar.test.coxb";

    /** Custom classes package. As referenced by LegStar Coxbgen. */
    public static final String CUST_PKG_PREFIX = "com.legstar.coxb.cust";

    /**
     * Create a CIXS structure.
     * 
     * @param serviceName the service name
     * @param jaxbTypeName the JAXB type name
     * @param cicsContainer CICS CONTAINER name or null if commarea
     * @param hasCustomCode true if COXB class references custom code
     * @return the new CIXS structure
     */
    public static CixsStructure createCixsStructure(final String serviceName,
            final String jaxbTypeName, final String cicsContainer,
            final boolean hasCustomCode) {
        CixsStructure structure = new CixsStructure();
        structure.setJaxbType(jaxbTypeName);
        structure.setJaxbPackageName(JAXB_PKG_PREFIX + "." + serviceName);
        structure.setCicsContainer(cicsContainer);
        if (hasCustomCode) {
            structure.setCustPackageName(CUST_PKG_PREFIX + "." + serviceName);
        }
        return structure;
    }

}
